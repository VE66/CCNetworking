// MARK: - 十六进制颜色
public func RGBValueColor(hex: String, alpha: CGFloat = 1.0) ->UIColor {
    var cString = hex.trimmingCharacters(in: .whitespacesAndNewlines) as NSString
    
    if cString.hasPrefix("#") {
        cString = cString.substring(from: 1) as NSString
    }else {
        return UIColor.white
    }
    
    let rString = cString.substring(with: NSRange(location: 0, length: 2))
    let gString = cString.substring(with: NSRange(location: 2, length: 2))
    let bString = cString.substring(with: NSRange(location: 4, length: 2))
    
    var r: CUnsignedInt = 0
    var g: CUnsignedInt = 0
    var b: CUnsignedInt = 0
    
    Scanner(string: rString).scanHexInt32(&r)
    Scanner(string: gString).scanHexInt32(&g)
    Scanner(string: bString).scanHexInt32(&b)
    
    return UIColor(red: CGFloat(r)/255.0, green: CGFloat(g)/255.0, blue: CGFloat(b)/255.0, alpha: alpha)
}

// MARK: - RGB颜色
public func RGBColor(r: CGFloat, g: CGFloat, b: CGFloat, a: CGFloat) ->UIColor{
    return UIColor(red: r/255.0, green: g/255.0, blue: b/255.0, alpha: a)
}

// MARK: - 随机色
public func RandColor() ->UIColor {
    return RGBColor(r: CGFloat(arc4random_uniform(255)), g: CGFloat(arc4random_uniform(255)), b: CGFloat(arc4random_uniform(255)), a: 1)
}

// MARK: - 背景色返回图片
extension UIImage {
    class func imageFromColor(color: UIColor, viewSize: CGSize) -> UIImage{
        let rect: CGRect = CGRect(x: 0, y: 0, width: viewSize.width, height: viewSize.height)
        UIGraphicsBeginImageContext(rect.size)
        let context: CGContext = UIGraphicsGetCurrentContext()!
        context.setFillColor(color.cgColor)
        context.fill(rect)
        let image = UIGraphicsGetImageFromCurrentImageContext()
        UIGraphicsGetCurrentContext()
        return image!
    }
    
    class func imageFromColor(color: UIColor) -> UIImage{
        let size: CGSize = CGSize(width: 1, height: 1)
        return self.imageFromColor(color: color, viewSize: size)
    }
    
